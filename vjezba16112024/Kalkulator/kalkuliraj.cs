namespace Kalkulator
{
    public class kalkuliraj
    {
        public double Zbroji(double a, double b)
        {
            return a + b;
        }

        public double Oduzmi(double a, double b)
        {
            return a - b;
        }

        public double Podjeli(double a, double b)
        {
            return a / b;
        }

        public double Pomnozi(double a, double b)
        {
            return a * b;
        }

    }
}
